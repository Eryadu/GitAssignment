package Feb22MethodConst;

public class MultiplicationDemo {
    public int multi (int a, int b)
    {
        return (a*b);
    }

    public int multi (int a, int b, int c)
    {
        return (a*b*c);
    }
    public int multi (int a, int b, int c, int d)
    {
        return (a*b*c*d);
    }
}
